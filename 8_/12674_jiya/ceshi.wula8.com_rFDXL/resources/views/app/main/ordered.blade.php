<script>
    function received_commission(){
        document.querySelector('.loadingClass').style.display='block';
        var rrc = document.querySelector('.rrc');
        rrc.innerHTML = 'Receiving..'
        $.ajax({
            url: "{{route('user.received.amount')}}",
            type: 'GET',
            dataType: 'json',
            success: function(res) {
                document.querySelector('.loadingClass').style.display='none';
                rrc.innerHTML = 'Received';
                message(res.message)
                document.querySelector('.bblance').innerHTML = res.balance
            }
        });
    }
