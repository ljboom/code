<script>
    function tabMenu(_this, tab){
        var tabBox1 = document.querySelector('#tabBox1');
        var tabBox3 = document.querySelector('#tabBox3');

        var tabs = document.querySelectorAll('.item');
        for (let i=0;i<tabs.length;i++){
            if (tabs[i].classList.contains('item_selected')){
                tabs[i].classList.remove('item_selected')
            }
        }
        _this.querySelector('.item').classList.add('item_selected');

        if (tab == 'one'){
            tabBox1.style.display='block';
            tabBox3.style.display='none';
        }
        if (tab == 'three'){
            tabBox1.style.display='none';
            tabBox3.style.display='block';
        }
    }
</script>
